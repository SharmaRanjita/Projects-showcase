			Day 3 - 
		
			<span> Transfer to Pelling</span>
<div class="expBut">-</div>
</div>
<p style="overflow: hidden;">
				Start the day with hearty breakfast at the hotel. After that pack your bags and check out from the hotel. Proceed by<br>
				road towards Pelling, the next stop of your North East holiday. En route pass by the beautiful Sikkimese town of<br>
				Jorethang.<br></p>
				<p style="overflow: hidden;">
				Heavenly scenery greets you upon reaching Pelling. Nestled amid spectacular views of Himalayas, at an altitude of<br>
				2072 m, the town is known for views of the snow-capped peaks of Pandim, Rathong, Kanchenjunga and Siniolchu.<br>
				The charming places to visit in Pelling include ancient Tibetan monasteries, pristine lakes, meadows of varieties of<br>
				Rhododendron and orchids.<br></p>
				<p style="overflow: hidden;">
				On arrival, check-in at a hotel. Enjoy the comforts of your hotel as rest of the day is free at your own leisure. Spend<br>
				the evening strolling through the small town or explore the colorful markets. Here find treasures of hand woven<br>
				carpets, shawls and blankets in Lepcha leaves, Buddhist religious items, thangkas, traditional motifs, and paper<br>
				products.<br></p>
				<p style="overflow: hidden;">
				Overnight stay in the hotel.</p>
