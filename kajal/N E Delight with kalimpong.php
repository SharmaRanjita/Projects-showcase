			Day 1 - 
		
			<span>Arrival and transfer to Darjeeling</span>
<div class="expBut">-</div>
			</div>
<p>
				Arrive at Bagdogra airport/ Jalpaiguri railway station/ Tenzing Norgay bus stand. Here you will be meet by our tour <br>
				representative who will assist you with transfer to Darjeeling.<br></p>
				<p>
				Set at 2050m in the lap of Himalayas, Darjeeling is named as the 'Queen of Hills'. Owing to its exotic location,<br>
				attractions and tourist spots the hill town is popular as a romantic honeymoon destination as well as family vacation<br>
				spots in India. The prime highlights are Darjeeling Himalayan Railway- a UNESCO World Heritage Site and several tea<br>
				estates.<br></p>
				<p>
				Upon arrival, check-in at a pre-booked hotel. You can opt either to relax at the hotel or choose to go shopping or for<br>
				a leisurely walk.<br></p>
				<p>
				Overnight stay in the hotel.</p>
	</div>
</div>
				
		<div class="duration-destination-bg" style="font-size:17px; margin-left:194px; margin-right:156px;">
	<div class="sys">
		<div class="days-vise">
			
			Day 2 - 
		
			<span>  Sightseeing in Kalimpong</span>
<div class="expBut">-</div>
               </div>
				<p style="overflow: hidden;">
				Start the day with breakfast at the hotel. Later get ready for sightseeing. Pass through scenic road to Forest museum<br>
				(closed on Thursday). Sightseeing tour includes visit to Deolo Hill, Hanuman Mandir, Dr. Graham Homes (school),<br>
				Durpin Monastery, Mangaldham and Pine View Nursery.<br></p>
				<p style="overflow: hidden;">
				The charming town of Kalimpong is set between Deolo Hills. Start the tour with a visit to the highest point of the<br>
				region, Deolo Hills 1704 m above the sea level. Enjoy the picturesque views of the mountains, especially the Mt<br>
				Kanchenjunga. Also, relish the taste of cherries cultivated here.<br></p>
				<p style="overflow: hidden;">
				Next, proceed for a visit to the Durpin Monastery in the lap of Durpin Dara hill. The monastery is known for two big<br>
				cylinders each roughly 10ft high, on both the sides. As you turn the cylinders by a crank, the lever is so arranged that<br>
				a bell would ring on each turn. Also rotate the row of prayer wheels for divine blessings.<br></p>
				<p style="overflow: hidden;">
				Among the best features of Kalimpong are the various attractive nurseries. Pay a visit to the Pine view nursery<br>
				boasting of a great collection of cactus in whole Asia. The visit is incredible owing to the presence of cactus in the<br>
				North Eastern hills.<br></p>
				<p style="overflow: hidden;">
				After a well-spent day sightseeing, return to your hotel for overnight stay.</p>
