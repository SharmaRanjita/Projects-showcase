                  Day 1 - 
		
			<span>Delhi (Arrival)</span>
<div class="expBut">-</div>
			</div>
				<p style="Overflow:hidden; display:block">

				On your arrival at Bagdodra airport/ Jalpaiguri railway station, meet our tour representative and get escorted to<br>
				pre-booked hotel in Gangtok.<br></p>
				<p style="Overflow:hidden; display:block;">
				Revered as the 'land of monasteries, Gangtok is one of the top tourist destinations to visit in India. The city boasts of<br>
				number of iconic monasteries, educational institutions, striking valleys and wonderful sanctuaries. With its<br>
				breathtaking attractions, Gangtok allures travelers from all parts of India and across the globe. The place has much<br> 
				to offer when it comes to attractions.<br></p>
				<p style="Overflow:hidden; display:block;">
				
				You may visit Rumtek Monastery; largest Gompa in Sikkim, Saramsa Garden (a beautiful garden where you can see<br>
				number of fruits and flowers planted), Fambong La Wildlife Sanctuary (wonderful sanctuary famous for varied flora <br>
				and fauna) and Yumthang valley (the valley is home to Shingba Rhododendron Sanctuary where there are 24 species<br> 
				of Rhododendron).<br></p>
				<p class="text-right" style="overflow: hidden; display: block;">
				<strong>Overnight stay in the Hotel</strong></p>
		</div>
