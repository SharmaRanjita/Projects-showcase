                   Day 2 - 
		
			<span>Excursion to Tsomgo Lake (38 km/ around an hour drive)</span>
<div class="expBut">-</div>
			</div>
			<p style="overflow: hidden; display: block;">
				After breakfast, proceed for short trip to Tsomgo Lake; one of the most popular tourist spots settled at an attitude <br>
				up to 12, 400ft. The lake receives its water from the melting of snows of the mountains. The lake holds great <br>
				religious importance among locals. Legends say that, Buddhist monks in earlier times predicted the future after the<br>
				study of color of water.<br></p>
				<p style="overflow: hidden; display: block;">
				You may also indulge in wonderful yak and mule ride along the lake and adore the spectacular surrounding. There is<br>
				also a Lord Shiva temple near the lake where you can offer sincere prayers to seek the blessings of the Lord.<br></p>
				<p style="overflow: hidden; display: block;">
				Next you head to Baba mandir which is another major tourist attraction in Gangtok. The holy shrine is placed at an<br>
				altitude up to 1300ft. and is just few kilometers away from Tsomgo Lake. Locals here claim that the temple was put<br>
				up in the memory of Baba Harbhajan Singh (a soldier). In fact, the temple is also referred to as Baba Harbhajan <br>
				Singh temple. With some good, memories return back to hotel. Rest of the day you can either shop at the lively <br>
				bazaars of Gangtok or just loosen up in the hotel room.<br></p>
				<p style="overflow: hidden; display: block;">
				Wel, if you are in Gangtok, shopping becomes a must-do thing. The best-buys of the place are handicrafts, locally <br>
				made artifacts, Tibetan carpets, shimmering jewelries, Sikkim tea, alpine cheese, black cardamom and other traditional outfits.<br></p>
				<p class="text-right" style="overflow: hidden; display: block;">
				<strong>Overnight stay in the Hotel</strong></p>
				</div>
				</div>
					
