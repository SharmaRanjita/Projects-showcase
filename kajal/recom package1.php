                   Day 3 - 
		
			<span>Darjeeling local Sightseeing (half day)</span>
<div class="expBut">-</div>
			</div>
			<p style="overflow: hidden; display: block;">
				Today early morning get ready for some thrilling drive to Tiger Hill; at a height up to 8,500 ft. (On a clear day),<br>
				cherish the mesmerizing natural beauty of the hill, the lush green jungles and the tranquil ambience. Usually, it is<br>
				noted that, visitors stopover this hill to relish the wonderful sunrise. The ultimate view of golden sunrays falling over<br> 
				the snow covered Himalayan ranges would surely leave you awestruck.<br></p>
				<p style="overflow: hidden; display: block;">
				From Tiger hill you can have a perfect view of Kanchenjunga, the third highest peak in the world. In case you are<br>
				lucky, you may also get to see the world's highest peak, Mount Everest.<br></p>
				<p style="overflow: hidden; display: block;">
				Thereafter, on the way back to hotel room, visit the Ghoom monastery-built by Lama Sherpa Gyaltsho, the<br>
				monastery belongs to Gelugpa (yellow hat) Sect. The monastery is highly revered among Buddhists and is<br>
				storehouse to several Buddhist manuscripts. The highlights of the monastery are thangkas on the walls and around<br>
				4.57m high statue of Maithreya Buddha. The serenity of the place would surely provide you great peace and power;<br>
				and then proceed to Batasia Loop and War Memorial. Situated inside Batasia Loop- a scenic garden, War Memorial is<br>
				a tribute to Gorkha soldiers of Darjeeling who sacrificed their life in the battle. Inaugurated in 1995, the memorial<br>
				holds an elevated platform where there is a statue of a soldier paying respect and a Cenotaph.<br></p>
				<p style="overflow: hidden; display: block;">
				The loop offers amazing views of Kanchenjunga and picturesque surrounding from toy train (route from Ghum and <br>
				Darjeeling). Getting back to hotel for breakfast and relax for some time. After breakfast, get ready for a half day tour<br>
				to Darjeeling.<br></p>
				<p style="overflow: hidden; display: block;">
				Begin with Padmaja Naidu Zoological Park which is famed for some rarely seen species including Red Panda,<br>
				Siberian Tiger, Tibetan Wolf, Black Bear and Snow Leopard. Located at a height up to 2133m, Padmaja Naidu is one of<br>
				famous and highest Zoo in India. The main idea behind the formation of the park was to conserve the endangered <br>
				species of fauna. The park has an amazing scenic beauty of lush jungles.<br></p>
				<p style="overflow: hidden; display: block;">
				Then we take you to Himalayan Mountaineering Institute (closed on Thursday)- an institute basically put up to<br>
				encourage today's generation students to improve participation in exciting sports like mountaineering; Passenger<br>
				Ropeway- one of the finest ways to adore the beautiful Darjeeling . It is India's first Passenger Ropeway starting<br>
				from Singamari. The route to Darjeeling and Singla Bazaar feels even better when you experience the ride.<br></p>
				<p style="overflow: hidden; display: block;">
				From the Cable Car, one can truly sense enchanting tea farms, breathtaking valley, waterfalls and the River Ranjeet<br> 
				and Ramman. Moreover, you can also have the splendid views of Eastern Himalayan Range and the snow covered<br>
				peaks of Kachenjunga; Tenzing and Gombu Rock Darjeeling- two big natural rocks set opposite to each other. Named<br>
				after Tenzing Norway is the Tenzing rock and Gombu rock named after Nawang Gombu. Both the rocks are training<br>
				locations for Himalayan Mountaineering Institute students; Tibetan Refugee Self Help Center-a perfect place to<br>
				explore Tibetan Culture through the artifacts, wooden carvings, photographs, carpets, leather work and much more<br>
				put into exhibition and on sale; Tea Estate- producing variety in teas, tea estates in Darjeeling are famous for their<br>
				well-maintained farms where you can see different flavors, taste and aroma of teas (Sunday closed); Peace Pogoda-<br>
				a Buddhist stupa/ monument specially put up to maintain peace and unite people of all religion and caste.<br>
				Constructed under the guidance of a Buddhist monk, Nichidastu Fuji, the stupa stands for world peace.<br></p>
				<p class="text-right" style="overflow: hidden; display: block;">
				<strong>Return back to hotel for overnight stay.</strong></p>
