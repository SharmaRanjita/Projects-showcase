                   Day 3 - 
		
			<span>Kalimpong Sightseeing (half day) - get transferred to Darjeeling (48km, around 2 hours drive)</span>
<div class="expBut">-</div>
			</div>
				<p style="overflow: hidden; display: block;">
				Fourth day is set out to explore Kalimpong. After breakfast, leave the hotel and drive through scenic road to Deolo <br>
				hill; listed among the important places of interest to visit in Kalimpong. Deolo hill is one of the two hills in between <br>
				which the town Kalimpong is setup. Scale the highest point of the region, Deolo hill, up to 1704m above the sea level.<br>
				From the top, relish the enchanting views of Teesta River, Relli valley and gorgeous surrounding.<br></p>
				<p style="overflow: hidden; display: block;">
				Then you head to Hanuman mandir; a big statue of Lord Hanuman on a hill. It is a great place for Hanuman devotees.<br>
				Afterwards, visit the popular school of the town, Dr. Graham's homes (school). The school was opened with a motive <br>
				to educate the children of tea workers and other poor kids. The Graham's home is also a well-known shooting spot.<br></p>
				<p style="overflow: hidden; display: block;">
				Later, proceed to some important tourist spots in Kalimpong including- Durbin Monastery which is placed on the<br>
				Durbin Dara Hill. The star attraction of the monastery is two large cylinders around 10ft high, on two sides. On<br>
				turning the cylinders by a crank, the lever is so arranged that a bell would prompt on every turn. You also get to see<br>
				prayer wheels on outside walls of the monastery; Manghal Dham, which a shrine cum a prayer hall. Dedicated to<br>
				Lord Krishna the temple was raised by Shri Krishna Pranami in the memory of Guru Sri 108 Mangaldasji. The temple<br>
				houses the statues of Prannath, Raj Shyamji and Mangaldasji.<br></p>
				<p style="overflow: hidden; display: block;">
				The divine Mangal Dham works on the principles to help the schools, orphanages and whosoever in need; and Pine<br>
				view nurseries which presents you a breathtaking and largest collection of cactus in whole Asia. The variety would<br>
				make you wonder about cactus as a possibility in North Eastern locations too, apart from desert areas.<br></p>
				<p style="overflow: hidden; display: block;">
				Later on, get transferred to Darjeeling.</p>
				<p style="overflow: hidden; display: block;">
				Renowned as the 'Queen of hills', Darjeeling is an important tourist destination to visit in the state of West Bengal.<br>
				The marvelous hill station is set up at an elevation of 7100ft with mountains and valleys all around and is best-<br>
				known for salubrious weather, natural beauty, tea farms, monasteries, lakes, museums and a whole lot. The awe-<br>
				inspiring views of Kanchenjunga, (the third highest mountain peak in the world) would totally leave you awestruck.<br></p>
				<p style="overflow: hidden; display: block;">
				Darjeeling is also home to the famous World Heritage Train- Darjeeling Himalayan Railway; a thin gauge course<br> 
				connecting Darjeeling and Siliguri. Popular as the Darjeeling Toy train, the train started its operation in around 1881<br>
				and is one of the few hill railways operating in India. The train passes through some rolling hills, green jungles and <br>
				tea farms of Darjeeling.<br></p>
				<p style="overflow: hidden; display: block;">
				On your arrival at Darjeeling, check-in at a hotel and relax. Further, in the evening you are free to walk around the<br>
				local markets. Some of the unique buys of Darjeeling that shouldn't be missed during your visit include variety of <br>
				teas, amazing kitchen wares, brass statues, woven fabrics and shinning jewelries.<br></p>
				<p class="text-right" style="overflow: hidden; display: block;">
				<strong>Overnight stay in the Darjeeling.</strong></p>
				</div>
				</div>
