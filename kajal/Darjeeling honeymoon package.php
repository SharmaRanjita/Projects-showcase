	<div class="sys">
		<div class="days-vise">
			
			Day 2 - 
		
<span> Gangtok sightseeing</span>
			<div class="expBut">-</div>
			</div>
				<p style="overflow: hidden; display: block;">
				The sixth day of the itinerary is for visiting the popular attractions of Gangtok, after morning breakfast Visit to<br>
				Directorate of Handicrafts &amp; Handloom, Research Institute of Tibetology, Do Drul Chorten (Stupa), Enchey Monastery,<br>
				White Hall, Flower Show, Ganesh Tok- a temple dedicated to Lord Ganesha, placed on a small hill on the Gangtok-<br>
				Nathula Road, Hanuman Tok, Tashi View Point, Banjhakri Falls, one of the finest falls close to Gangtok, place amidst<br>
				a lush green well-maintained Shamanistic- themed park.<br></p>
				<p style="overflow: hidden; display: block;">
				One of Sikkim's most renowned temples, Hanuman Tok is dedicated to Lord Hanuman. The temple complex is well<br>
				taken care of by the Indian Army. As the temple is placed on high altitudes, the complex offers wonderful<br>
				opportunities for photography as the views of the peaks are stunning from here.<br></p>
				<p style="overflow: hidden; display: block;">
				Probably the biggest stupa of Sikkim, Do Drul Chorten is located on a hill. This sacred spot was built in the year 1945<br>
				by Late Truslshi Rimpoche, the head of Nyingma sect of Tibetan Buddhism. The golden-colored dome is the highlight<br>
				of the structure. You can spot lamas engrossed in their prayers.<br></p>
				<p style="overflow: hidden; display: block;">
				Founded in the year 1909, Enchey Monastery is one of the Gangtok's most popular Gompas. Placed on a hill, the<br>
				monastery offers exciting views of Mt Kanchenjunga. The structure of the monastery is inspired from a Chinese<br>
				Pagoda and the images of deities adorn the inner walls. Spot the statues of Guru Rinpoche and Lord Buddha, who<br>
				are worshipped here.<br></p>
				<p style="overflow: hidden; display: block;">
				If you are a nature lover, do visit the permanent flower show held at the Flower Exhibition Center, another popular<br>
 				attraction. The floral exhibition site is near Ride Park and White Hall, and is a place where you must not miss clicking<br>
				photographs. This exhibition area has a green house effect and consists of a vast variety of floral species such as<br>
				celosia, gerberas, roses, orchids and more.<br></p>
				<p style="overflow: hidden; display: block;">
				During the night hours, you can visit the discotheques in Gangtok. Stay overnight.</p>
