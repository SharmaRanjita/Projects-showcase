			</div>
				<p style="overflow: hidden; display: block;">
				Arrive in Siliguri at New Jalpaiguri Railway Station (68 km) / Bagdogra Airport (69 km) / Tenzing Norgay Bus Stand<br>
				(62 km), met our tour representative. He would help you in taking an assisted transfer to Darjeeling.<br></p>
				<p style="overflow: hidden; display: block;">
				Home to the World Heritage Train- the Darjeeling Himalayan Railway (DHR) or the toy train, Darjeeling is an<br>
				enchanting hill station. Lying at a height of 7100 feet above the sea level, Darjeeling has aromatic lush tea estates,<br>
				tranquility in abundance, breathtaking scenery, Buddhist monasteries, bustling streets and several striking views.<br></p>
				<p style="overflow: hidden; display: block;">
				After you arrive in Darjeeling, check- into the hotel. You can relax for a while. The evening is at leisure and you can<br>
				spend it as per your own liking. Have a leisure walk up to the Mall or visit the local shopping centers to get your<br>
				hands on some memorabilia. Over Night stay at Darjeeling.<br></p>
