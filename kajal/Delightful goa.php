			Day 2 - 
		
			<span> North Goa</span>
<div class="expBut">-</div>
</div>
<p style="overflow: hidden;">
				This morning, you have your breakfast and then start driving towards North Goa. North Goa is dotted with some of<br>
				the most beautiful beaches of Goa. Dona Paula, Calangute, Baga and Miramar Beach are popular beaches of North<br>
				Goa. North Goa is home to a number of tourist attractions like Fort Aguada, Basilica of Bom Jesus and Se Cathedral.<br></p>
				<p style="overflow: hidden;">
				Spend the day sightseeing around North Goa. You can begin with a visit to "Cidade de Goa", the original name given to<br>
				the 'city of Goa' which was established long back and now it is called "Old Goa". Pay homage at the Basilica Bom<br>
				Jesus, which is dedicated to Infant Jesus. The church has the sacred remains of St Francis Xavier. Also, visit other<br>
				churches of North Goa including St Cajetan, St Francis of Assisi, Se Cathedral and Church of St. Monica. All the<br>
				churches feature a unique architectural style which speaks much about the heritage and craftsmanship of those<br>
				times.</p>
				<p style="overflow: hidden;">
				Visit the Museum of Christian Art, which is one of its kind museum in Asia. The museum is best known to house a<br>
				collection of items like gold and precious stones and Rosary's. You can also visit Archeological Museum.<br></p>
				<p style="overflow: hidden;">
				Later, travel to "Solar Souto Maior", which is an aristocratic mansion which dates back to the year 1585. The mansion<br>
				houses a treasure of artifacts.<br></p>
				<p style="overflow: hidden;">
				Return to Panjim, the capital of Goa and as you drive through the city enjoy viewing its unique colonial architecture.<br>
				Walk through the city and flip back the pages of the Portuguese days. Do visit the bustling markets of Goa and shop<br>
				for gifts and souvenirs. Enjoy your lunch at Panjim.<br></p>
				<p style="overflow: hidden;">
				Post lunch, head to Baga Beach, which is one the most popular, happening and beautiful beaches of Goa. Enjoy water<br>
				sports and then you can go for some shopping at the Tibetan market.<br></p>
				<p style="overflow: hidden;">
				Stay overnight at the hotel.</p>
