		<div class="days-vise">
			
			Day 3 - 
		
			<span> South Goa</span>
<div class="expBut">-</div>
</div>
<p style="overflow: hidden;">
				After having your breakfast, head towards South Goa. Today, you will be sightseeing at major attractions of South<br>
				Goa including Colva Beach, Mangeshi Temple, Santa Durga Temple and some more attractions. Begin with a visit to<br>
				Margao, the capital of Salcette taluka and headquarters of the South Goa district.<br></p>
				<p style="overflow: hidden;">
				Visit the historical "Largo de Igreja", the Church square of Margao, which once used to be its colonial quarter. The<br>
				church square is dotted with beautiful old buildings on one side and the beautiful Church of the Holy Spirit on the<br>
				other. The Church of the Holy Spirit is one of the most grant churches of Goa, which was built back in the year 1564.<br>
				The church is one of the finest examples of Indian Baroque style of architecture.<br></p>
				<p style="overflow: hidden;">
				Later proceed to "Ancestral Goa", which is a re-creation of Goan rural life. The village has number of artifact centers,<br>
				art galleries and open air museums. There is a laterite sculpture of Saint Mirabai in the village, which is one of the<br>
				longest laterite sculptures in India. The star attraction of the region is the "big foot", which is a large foot print which<br>
				was discovered on a rock while excavating the site.<br></p>
				<p style="overflow: hidden;">
				Now it's time to visit the grand house- Menezes Braganza house, in Chandor. This palatial structure was built back in<br>
				the 17th century. The house has now been divided into two separate houses, with a common entrance. The east wing<br>
				is occupied by the Pereira-Braganza family, which has a small chapel with relics of St. Francis Xavier. It also has<br>
				artifacts, collected by the family over a number of years. The house also has a large ballroom and a Salon. Tiles from<br>
				Portugal, antique chandeliers from Europe and beautifully carved, ornate rosewood furniture adds to the beauty of<br>
				the house. A few things which give a distinct look to the house include a pair of high-backed chairs, which has the<br>
				family crest, which was given to the Pereira-Braganza family by King Dom Luis of Portugal. Most of the furniture<br>
				items set in the house date back to the 18th century and have been prepared using local seeso (martel wood) and<br>
				lacquered with mother of pearl by craftsmen from Curtorim village. The house also has many items coming from<br>
				antique aficionados.</p>
				<p style="overflow: hidden;">
				The west wing of the house belongs to the Menezes Braganza family. The house not only features exquisite furniture<br>
				and Chinese porcelain from Macau, but also a collection of family portraits, which date back to the 17th and 18th<br>
				centuries. There is also a library which is believed to be the first private library of Goa. The library has a collection of<br>
				5,000 leather bound books which were personally collected by Luis de Menezes Braganza (1878-1938), a reputed<br>
				journalist, well-known for playing a key role in Goa's independence movement.<br></p>
				<p style="overflow: hidden;">
				After the long sightseeing tour of South Goa, head back to the hotel. Stay overnight at the hotel.<br></p>
