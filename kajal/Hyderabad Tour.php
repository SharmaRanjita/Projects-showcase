<p style="overflow: hidden; display: block;">
				On your arrival at Hyderabad airport, meet our IHPL guide. Begin your tour by visiting the beautiful monument, Char<br>
				Minar; it is an amazing blend of granite, lime and mortar around which the state of Hyderabad started.<br></p>
				<p style="overflow: hidden; display: block;">
				With four great gates, Char Minar is the oldest mosque in the Hyderabad. You then head to Mecca Mosque, the<br>
				biggest Mosque in Hyderabad. The structure of the mosque is similar to that of Mecca Madina in Saudi Arabia. It is<br>
				one of the largest mosques in India, which can accommodate thousand of devotees at a time.<br></p>
				<p style="overflow: hidden; display: block;">
				Further, you visit the Qutaub Shahi Tomb, which represents the architectural tradition of Qutub Shahi dynasty. The<br>
