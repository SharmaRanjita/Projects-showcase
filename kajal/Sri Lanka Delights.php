			Day 3 - 
		
			<span>Nuwara Eliya – Colombo</span>
<div class="expBut">-</div>
</div>
<p style="overflow: hidden; display: block;">
				Once done with a hearty breakfast, get ready for Colombo, the city with a long history. After reaching, complete the<br>
				check-in formalities at the hotel in Colombo. The day is scheduled for sightseeing tour.<br></p>
				<p style="overflow: hidden; display: block;">
				Marvel at the clock tower which is around 100 years old. Explore numerous buildings built during the British era.<br>
				Next, proceed towards the Pettah bazaar. Walking around this vibrant bazaar will give you an illusion of being<br>
				present in the Cairo or Tehran. Pay homage at a temple of Buddhist followers. Wander in the posh residential areas.<br></p>
				<p style="overflow: hidden; display: block;">
				Post this, get ready to visit the National Museum of Colombo. The largest museum in Sri Lanka preserves the things<br>
				which are of much importance to the island. Spend some time at the Bandaranaike Memorial International<br>
				Conference Hall, the very first purpose built convention center in Asia. Viewing the new parliament is also in the list.<br>
				Get insights about the Dutch colonial rule in Sri Lanka by visiting the Colombo Dutch Museum.<br></p>
				<p style="overflow: hidden; display: block;">
				After an engaging day, return to the hotel for an overnight stay.</p>
