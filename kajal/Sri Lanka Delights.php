			Day 2 - 
		
			<span>Kandy - Peradeniya - Nuwara Eliya</span>
<div class="expBut">-</div>
</div>
<p style="overflow: hidden; display: block;">
				The first stopover of Day 2 of Sri Lanka Delights tour will be at the Royal Botanical Garden at Peradeniya. Attracting<br>
				millions of visitors annually, the garden is famous for its collection of orchids. Soak-in the beauty of more than 4000<br>
				species of plants here.</p>
				<p style="overflow: hidden; display: block;">
				Post this, hit the roads to the Nuwara Eliya often termed as the ‘Little England’. To make the trip memorable, a visit<br>
				to the tea plantation en route is scheduled for you. Upon arrival at the hotel in Nuwara Eliya, you can complete the<br>
				check-in formalities. Afterwards, get ready for an exciting city tour.<br></p>
				<p style="overflow: hidden; display: block;">
				Treat your eyes and senses to the tea plantation and visit the tea factory. Witness how tea is prepared and graded.<br>
				Taste freshly brewed pure Ceylon tea. This will surely remove all your tiredness. The pristine mountain beauty and<br>
				golf courses will cast a spell on you.<br></p>
				<p style="overflow: hidden; display: block;">
				Return to the hotel for an overnight stay.</p>
	</div>
</div>
