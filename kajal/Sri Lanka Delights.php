		<div class="duration-destination-bg" style="font-size:17px; margin-left:110px; margin-right:130px;">
	<div class="sys">
		<div class="days-vise">
			
			Day 1 - 
		
			<span>Colombo - Pinnawala – Kandy</span>
<div class="expBut">-</div>
			</div>
<p style="overflow: hidden; display: block;">
				Upon arrival at the Bandaranaike International Airport, meet our tour representative who will guide you with<br>
				assisted transfer to the hotel in Kandy. Pay a visit to the Pinnawala Elephant Orphanage en route. One-of-its-kind,<br>
				the Pinnawala Elephant Orphanage is a nursery, orphanage and captive breeding ground for wild Asian elephants.<br>
				Continue your journey towards the hotel. On reaching, complete the check-in formalities.<br></p>
				<p style="overflow: hidden; display: block;">
				Get ready for the sightseeing in Kandy, a World Heritage Site. You’ll be amused to see how the traditions, legends<br>
				and folklore of Kandy are still kept alive. The first destination will be Kandy Lake. Also known as the ‘Sea of Milk’, the<br>
				artificial lake has many legends associated with it. Thereafter, you will be taken to the local market and free to enjoy<br>
				shopping for some time.<br></p>
				<p style="overflow: hidden; display: block;">
				Next on the itinerary is the Temple of the Tooth Relic. Nestled inside the royal palace complex of the former<br>
				Kingdom of Kandy, the Temple of the Sacred Tooth Relic is a Buddhist temple. The palace preserves the relic of the<br>
				tooth of the Buddha. Partake in the rituals which take place three times a day.<br></p>
				<p style="overflow: hidden; display: block;">
				Spare some time for visiting Kandy Town and Gem Museum. Post this, you will be taken to the Kandyan Art and Craft<br>
				center. Try buying something for you as a souvenir from here. Post this you will be entertained by a cultural<br>
				performance.</p>
				<p style="overflow: hidden; display: block;">
				Enjoy an overnight stay in the hotel at Kandy.</p>
	</div>
</div>

<div class="duration-destination-bg" style="font-size:17px; margin-left:110px; margin-right:130px;">
