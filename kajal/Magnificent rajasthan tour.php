		<div class="days-vise">
			
			Day 2 - 
		
			<span> Jodhpur to Jaisalmer (Drive)</span>
<div class="expBut">-</div>
</div>
<p style="overflow: hidden; display: block;">
				Today, check out of the hotel and drive to the next destination 'Jaisalmer'. Founded by Maharana Jaisal Singh, a<br>
				Rajput Singh in 1156 AD, the city derives its name from the ruler itself.<br></p>
				<p style="overflow: hidden; display: block;">
				A part of the Great Indian Thar Desert, the city is dotted by several Havelis that were built by the royal merchants of<br>
				the city.</p>
				<p style="overflow: hidden; display: block;">
				Upon arrival in Jaisalmer, check into the hotel. The rest of the day is free for leisure activities. Stay overnight in the<br>
				hotel.</p>
	</div>
</div>
       <div class="duration-destination-bg" style="font-size:17px; margin-left:110px; margin-right:130px;">
	<div class="sys">
		<div class="days-vise">
			
			Day 3 - 
		
			<span> Jaisalmer (Sightseeing)</span>
<div class="expBut">-</div>
</div>
<p style="overflow: hidden; display: block;">
				Today, in the morning, embark on a sightseeing tour of Jaisalmer. The city is quite famous for the Jaisalmer Fort,<br>
				also known as Sonar Quila. The town bustles with various shops, hotels and ancient havelis and is considered as the<br>
				only living fort in India. Inside this fort, visit the Raj Mahal (royal palace), the Laxminath Temple and several Jain<br>
				temples. The palace museum has a huge collection of the items belonging to the royal era.<br></p>
				<p style="overflow: hidden; display: block;">
				Other attractions that you will visit include Patwon-ki-Haveli, Gadsisar Lake (built in the 14th century by Maharawal<br>
				Gadsi for the conservation of rain water) and Sam Sand Dunes.<br></p>
				<p style="overflow: hidden; display: block;">
				Later in the evening, drive 45 km to the west of the city to visit Sam Sand Dunes. A camel ride to the Sunset Point<br>
				(direct payment basis) can be an unforgettable experience.<br></p>
				<p style="overflow: hidden; display: block;">
				Thereafter in the evening, drive 45 kilometers to the west of the city to visit Sam Sand Dunes. A camel ride to the<br>
				Sunset Point is an experience par excellence. Stay overnight in the hotel.<br></p>
	</div>
</div>

 <div class="duration-destination-bg" style="font-size:17px; margin-left:110px; margin-right:130px;">
	<div class="sys">
		<div class="days-vise">
			
			Day 4 - 
		
			<span>Jaisalmer to Bikaner (Drive)</span>
            <div class="expBut">-</div>
    </div>
<p style="overflow: hidden; display: block;">
				In the morning, proceed to the next destination-Bikaner. A vast expanse of glistening desert, the city lies in the<br>
				northern region of the state. It was founded by the Rathore Prince- Rao Bikaji in 1488 AD. Famous for Camel Safaris,<br> 
				it boasts of the world's largest Camel research and breeding farm.<br></p>
				<p style="overflow: hidden; display: block;">
				After reaching Bikaner, take a trip to the massive Junagarh Fort. The fort was built in 1593 AD by Raja Rai Singh- the<br>
				sixth ruler of the city. Some of the striking palaces inside the fort are Anup Mahal, Ganga Niwas and Rang Mahal.<br>
				Moreover, you can discover the rich treasures of the bygone era in the museum.<br></p>
				<p style="overflow: hidden; display: block;">
				Thereafter, visit the Laxmi Niwas Palace, an erstwhile residence of the former ruler- Maharaja Ganga Singh. It is a<br>
				wonderful example of Indo-Saracenic style and now serves as a luxury hotel.<br></p>
				<p style="overflow: hidden; display: block;">
				Thereafter, also visit the National Research Center on Camel. Spread over an area of more than 2000 acres, it is one<br>
				of its kinds in Asia. While spending your time here, you can enrich your knowledge about Camel, the famous 'Ship of<br>
				the Desert'. The centre also has a camel museum that throws light on the developmental aspects of the research <br>
				work.</p>
	</div>
</div>


 <div class="duration-destination-bg" style="font-size:17px; margin-left:110px; margin-right:130px;">
	<div class="sys">
		<div class="days-vise">
			
			Day 5 - 
		
			<span>Jaipur (Sightseeing)</span>
            <div class="expBut">-</div>
    </div>
<p style="overflow: hidden;">
				In the morning, embark on a road journey to the Amber Fort. En-route to the fort, halt for a visit to Hawa Mahal.<br>
				Hawa Mahal is a prominent tourist attraction in the city. The windows of the monument built through honeycombed<br>
				pink sandstone were once used by the royal ladies to watch royal processions passing the streets.<br></p>
				<p style="overflow: hidden;">
				Feel the luxury enjoyed by the royals as you take an elephant ride up to the Amber Fort. Nestled atop of a hill, the<br>
				grand fort impresses visitors at the first sight. Discover the history as you move through large gates, expansive<br>
				courtyards and palatial period rooms.<br></p>
				<p style="overflow: hidden;">
				Later, check out of the hotel and return back home with everlasting memories from the trip.<br></p>
