		<div class="days-vise">
			
			Day 1 - 
		
			<span> Jodhpur (Arrival)</span>
<div class="expBut">-</div>
			</div>
		<p style="overflow: hidden; display: block;">
				Upon your arrival in Jodhpur, check into the hotel. Located in the western region of Rajasthan, Jodhpur is one of the<br>
				most popular cities in the state of Rajasthan. It is also known as the 'Blue City' because it has many houses painted<br>
				in blue.</p>
				<p style="overflow: hidden; display: block;">
				The magnificent and formidable Mehrangarh Fort dominates the sky line of the city. Built entirely of red sandstones,<br>
				the fort is perched atop of a hill. Standing as a testimony to various battles and bloodshed, the strong walls of the<br>
				fort seem to have many secrets buried in. The fort has palatial period rooms, extensive courtyards and alluring<br>
				latticed windows. Several sections of the fort preserve the priceless belongings of the royals such as arms,<br>
				palanquins, artifacts and textiles.</p>
				<p style="overflow: hidden; display: block;">
				Later, take a short walk from the Mehrangarh Fort to reach another prominent landmark of the city-Jaswant Thada.<br>
				It is a shimmering white marble mausoleum that was built in the memory of Maharaja Jaswant Singh II.</p>
				<p style="overflow: hidden; display: block;">
				Jodhpur is equally popular for its local markets. The Sardar Market is located near the clock tower in the city and is<br>
				quite famous for its textiles in different prints, leather footwear and artifacts. Apart from exploring the local<br> 
				markets, the astonishing art &amp; craft will give you a brief about the city's true essence.<br></p>
				<p style="overflow: hidden; display: block;">
				Later, return to the hotel for overnight stay.</p>

	</div>
</div>

<div class="duration-destination-bg" style="font-size:17px; margin-left:110px; margin-right:130px;">
