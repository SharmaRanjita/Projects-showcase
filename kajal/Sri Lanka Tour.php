		<h2 class="h2" ><font color="red">Ltinerary</h2></font>
		<div class="duration-destination-bg" style="font-size:17px; margin-left:110px; margin-right:130px;">
	<div class="sys">
		<div class="days-vise">
			
			Day 1 - 
		
			<span>Arrival in Colombo - Kandy (3 hours)</span>
<div class="expBut">-</div>
			</div>
<p style="overflow: hidden; display: block;">
				Upon arrival at the Bandaranaike International Airport, meet our tour representative who will help you with guided<br>
				transfer to Kandy. There will be a halt at the Pinnawala Elephant Orphanage en route. The nursery, orphanage and<br>
				captive breeding ground for wild Asian elephants provides shelter to the injured, abandoned and orphaned<br>
				elephants. Watch the herds of elephants playing and bathing in the river nearby.<br></p>
				<p style="overflow: hidden; display: block;">
				Thereafter, continue your journey towards Kandy. Tucked beautifully on a plateau bounded by mountains,<br>
				comprising biodiverse rainforest and tea plantations, the city is a haven for nature lovers. Counted as UNESCO World<br>
				Heritage Site, Kandy boasts of splendid lake, lush grasslands, mist-clad mountains and Sri Lanka's longest river.<br>
				Complete the check-in formalities after reaching the hotel.<br></p>
				<p style="overflow: hidden; display: block;">
				Post this, get ready for a sightseeing in Kandy. The first attraction of the tour is Temple of the Tooth. Also known as<br>]
				the Temple of the Sacred Tooth Relic and Sri Dalada Maligawa, the holy place is also one of the most revered<br>
				Buddhist sites in the world.</p>
				<p style="overflow: hidden; display: block;">
				After this, proceed towards the Peradeniya Botanical Gardens. Fascinating millions of visitors with its collection of<br>
				orchids, the garden is a paradise for nature enthusiasts. Walk around more than 4000 species of plants and check<br>
				how many of them you can recognize. Next on the line is a visit to the Gem Museum. The museum is known for<br>
				producing world's finest quality semi-precious stones and gems including sparkling sapphires in different colors like<br>
				white, green, red, blue, etc.</p>
				<p style="overflow: hidden; display: block;">
				A traditional cultural dance show is scheduled for the evening.</p>
				<p style="overflow: hidden; display: block;">
				Enjoy an overnight stay in the hotel at Kandy.</p>
	</div>
</div>

<div class="duration-destination-bg" style="font-size:17px; margin-left:110px; margin-right:130px;">
	<div class="sys">
		<div class="days-vise">
			
			Day 2 - 
		
			<span> Kandy – Nuwara Eliya (2.5 hours)</span>
<div class="expBut">-</div>
</div>
<p style="overflow: hidden; display: block;">
				Enjoy a healthy platter of breakfast at the hotel. Subsequently, proceed towards the Nuwara Eliya, also titled as the<br>
				‘City of Lights’. Dotted with hills and lush tea plantations, the city features colorful meadows, pleasant weather,<br>
				green ambiance and scenic views of gorges and mountains. Titled as ‘Little England’, the place was favorite cool-<br>
				climate escape for the laborious Scottish and English pioneers of Sri Lanka’s tea industry. Check-in at the hotel upon<br>
				reaching.</p>
				<p style="overflow: hidden; display: block;">
				Begin the day trip to Nuwara Eliya by visiting the Victoria Park, a scenic park which was named to commemorate the<br>
				diamond jubilee of Queen Victoria. Afterwards, explore the golf course and the trout streams. Delight-in a boat ride<br>
				and fishing at Lake Gregory, a historic landmark and a must-visit destination. Get ready to understand how the finest<br>
				Orange Pekoe tea is prepared from the slow-growing bushes here by visiting a tea factory.<br></p>
				<p style="overflow: hidden; display: block;">
				Your next visit is to the Haggala Gardens, a temperate hill-country garden which is a home to lotus and water lily<br>
				floating in pristine environs. From there, you will be transferred to the next stopover which is at the Seetha Eliya, a<br>
				temple from the great Hindu epic Ramayana.<br></p>
				<p style="overflow: hidden; display: block;">
				Return to the hotel for an overnight stay.</p>
	</div>
</div>
       <div class="duration-destination-bg" style="font-size:17px; margin-left:110px; margin-right:130px;">
	<div class="sys">
		<div class="days-vise">
			
			Day 3 - 
		
			<span> Nuwara Eliya – Bentota (5.5 hours)</span>
<div class="expBut">-</div>
</div>
<p style="overflow: hidden; display: block;">
				Post a healthy breakfast, leave for Bentota, an astounding coastal town in Sri Lanka. Known as the ‘City of Golden<br>
				Beaches’, the place offers memorable experiences. Unwind and spend some cherishing moments with your near and<br>
				dear ones. Make your trip exciting by partaking in water sports activities, exploring coral reefs on glass boat and<br>
				witnessing several historical sites. After reaching, complete the check-in formalities and spend rest of the day<br> 
				sightseeing and engaging into fun activities. Take a tour of the popular beaches to bask-in the sunshine. Enjoy<br>
				strolling along the shores getting mesmerized by the scenic beauty and tranquility.<br></p>
				<p style="overflow: hidden; display: block;">
				Next, you will be treated with boating on river Madu where you will be beguiled by the scenic charm and lush<br>
				mangroves encompassing many islets. Extend your day trip by visiting the Turtle Hatchery, if you want to.<br>
				Inaugurated in 1981 by the Wildlife Protection Society, the hatchery is main site for turtle nesting. Spotting newly<br>
				born turtles in large tanks is a unique experience. These turtles are safely set free in the sea after 2 to 4 days.<br></p>
	</div>
