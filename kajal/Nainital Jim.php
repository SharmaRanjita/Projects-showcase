			Day 2 - 
		
			<span>  Nainital</span>
<div class="expBut">-</div>
</div>
<p style="overflow: hidden; display: block;">
				On day 2, savor delicious breakfast and you can begin to sightsee the attractions in and around Nainital. Rejuvenate<br>
				your senses with the ethereal charm of the verdant jungles, lush valleys, soft white clouds, stunning vistas and so<br>
				much more. Pay homage at the Hanumangarhi Temple. Dedicated to Lord Hanuman, the sacred shrines offers<br>
				astounding sunrise and sunset views.<br></p>
				<p style="overflow: hidden; display: block;">
				Next, you can calm your senses at the popular lakes in Nainital. Take a tour of Bhimtal, Sattal and Naukuchiatal. A<br>
				beautiful gem of Nainital, the Bhimtal is adorned with a cerulean lake and temples nestled amid the green<br>
				mountains. Sattal, on the other hand, is an interconnected group of seven freshwater lakes. Tucked beautifully<br>
				amidst hills, Naukuchiatal is truly surreal and is also known as the lake of nine corners. Don't forget to delight in<br>
				boat rides here.</p>
		<p style="overflow: hidden; display: block;">
				Enjoy dinner and an overnight stay at the hotel.</p>		
	</div>
</div>
