			Day 3 - 
		
			<span>Nainital – Corbett</span>
<div class="expBut">-</div>
</div>
<p style="overflow: hidden; display: block;">
				Day 3 starts with a hearty breakfast. Thereafter, check out from the hotel and get ready for your trip to Jim Corbett.<br>
				Nestled in Nainital, Jim Corbett National Park was established in the year 1936. Reckoned as the first national park<br>
				in the Asian continent, it is home to a variety of flora and fauna, including endangered species and Royal Bengal<br>
				Tigers. The oldest national park in India derived its name from a hunter turned conservationist Edward James<br>
				Corbett (Jim Corbett). It was the very place where Project Tiger was launched in 1973. Presently Corbett is divided<br>
				into five different zones where visitors can delight in wildlife safaris.<br></p>
				<p style="overflow: hidden; display: block;">
				Upon reaching, check-in at the hotel. Unwind yourself and proceed for evening safari in the park. Enjoy spotting<br>
				various species of flora and fauna. Rest of the day is to be spent leisurely. Satiate your hunger with tasty dinner and<br>
				retire for the night.<br></p>
				<p style="overflow: hidden; display: block;">
				An overnight stay is planned at the hotel.</p>
	</div>
</div>

