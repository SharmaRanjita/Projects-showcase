<p style="overflow: hidden; display: block;">
				The Nainital with Corbett tour starts from Delhi. Hit the road to Nainital. A picture hill station in Uttarakhand,<br>
				Nainital is blessed with oodles of natural wonders. Perched at a height of 6,358 feet above sea level, the hill town<br>
				lures tourists from different nooks and corners of the globe. The place is also quite popular amongst the<br>
				honeymooners.</p>
				<p style="overflow: hidden; display: block;">
				Upon arrival, check-in at the hotel. Spend the rest of the day relaxing. But, if you want, you can also go for exploring<br>
				the attractions nearby. Or else, you can visit Mall Road. Enjoying a boat ride on the Naini Lake is also an option.<br></p>
				<p style="overflow: hidden; display: block;">
				A scrumptious dinner and an overnight stay are planned at the hotel.</p>
