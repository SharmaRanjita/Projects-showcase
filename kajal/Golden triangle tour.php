			Day 1 - 
		
			<span> Delhi</span>
<div class="expBut">-</div>
			</div>
		<p>
				Arrive at the New Delhi International Airport. Meet your tour representative. Get assisted transfer to your hotel.<br>
				Check-in and relax. The day is free of plans that you can spend according to your wish. Opt to enjoy sightseeing,<br>
				dining, shopping or other activity of your choice.<br></p>
				<p>
				Overnight stay at the hotel.</p>
	</div>
</div>

<div class="duration-destination-bg" style="font-size:17px; margin-left:110px; margin-right:130px;">
	<div class="sys">
		<div class="days-vise">
			
			Day 2 - 
		
			<span>Delhi - Agra</span>
<div class="expBut">-</div>
</div>
<p style="overflow: hidden;">
				In the morning, check out of the hotel. Proceed to drive to Agra with a stopover at Sikandara. Set in the suburbs of<br>
				Agra, the town is home to Akbar's mausoleum.<br></p>
				<p style="overflow: hidden;">
				Later, get back to the hotel for overnight stay.</p>
	</div>
</div>
       <div class="duration-destination-bg" style="font-size:17px; margin-left:110px; margin-right:130px;">
	<div class="sys">
		<div class="days-vise">
			
			Day 3 - 
		
			<span>  Agra</span>
<div class="expBut">-</div>
</div>
<p style="overflow: hidden;">
				After freshening up for the day, proceed for a sightseeing tour. Pay a visit to Taj Mahal, one of the famous<br>
				monuments in the world. The Agra Fort, and Itmad-Ud-Daula's Tomb are equally impressive sights.<br></p>
				<p style="overflow: hidden;">
				In the evening, you can opt for shopping, eating or attending Light and Sound shows.<br></p>
				<p style="overflow: hidden;">
				Stay back at hotel for the night.<br></p>
	</div>
</div>

 <div class="duration-destination-bg" style="font-size:17px; margin-left:110px; margin-right:130px;">
	<div class="sys">
		<div class="days-vise">
			
			Day 4 - 
		
			<span>  Agra - Jaipur</span>
            <div class="expBut">-</div>
    </div>
<p style="overflow: hidden;">
				Start the day with packing your bags and leaving for Jaipur. Stop over at Fatehpur Sikri, the abandoned city of<br>
				Mughals that has a number of interesting sights. Start with exploring the mighty Buland Darwaza, one of the largest<br>
				doorways in the world. Move on to explore the amazing sights of Jama Masjid, Palace of Jodha Bai, Birbal Bhavan<br>
				and more.</p>
<p style="overflow: hidden;">
				Later, proceed with drive to Jaipur. On you arrival, check in at the hotel. Freshen up and embark upon a city tour.<br> 
				Explore the City Palace in the heart of the city, inhabited by the royal family till date. The other attractions to check<br>
				out are Jantar Mantar, Nawab Sahab Ki Haveli and Local Bazaar.<br></p>
<p style="overflow: hidden;">
				Return to the hotel for overnight stay.<br></p>				
	</div>
</div>
 <div class="duration-destination-bg" style="font-size:17px; margin-left:110px; margin-right:130px;">
	<div class="sys">
		<div class="days-vise">
			
			Day 5 - 
		
			<span> Jaipur</span>
            <div class="expBut">-</div>
    </div>
<p style="overflow: hidden;">
				Today's plan include sightseeing in the 'Pink City' Jaipur. Enjoy a wonderful elephant ride to the beautiful Amber<br>
				Fort. Listed as one of the hill forts of Rajasthan, a UNESCO World heritage Site, the fort is an incredible showcase of<br>
				Rajputana architecture with Mughal touches.<br></p>
				<p style="overflow: hidden;">
				Later pay a visit to Hawa Mahal, the five-storied pyramid shaped palace. Explore the markets to have a go at shopping.<br></p>
				<p style="overflow: hidden;">
				Get back to the hotel for a comfortable stay.</p>
	</div>
</div>

 <div class="duration-destination-bg" style="font-size:17px; margin-left:110px; margin-right:130px;">
